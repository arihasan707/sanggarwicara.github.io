                        <h4>Terapi okupasi (Occupational Theraphy) </h4>
                        <p>Menangani anak dengan gangguan perkembangan motorik halus</p>
                        <h4>Terapi Sensori Integrasi </h4>
                        <p>Menangani anak-anak dengan disfungsi sensory integrasi yang bisa terjadi pada kasus-kasus seperti autisme, Attention Deficit/Hyperactivity Disorder (ADHD), PDDNOS, Learning Disabilities, Asperger Syndrome dan lain-lain. Aktivitas integrasi sensoris juga bertujuan untuk meningkatkan kapasitas untuk belajar.</p>
                        <h4>Terapi Wicara (Speech therapy) </h4>
                        <p>Untuk anak dengan keterlambatan perkembangan bicara terapi bagi anak dengan keterlambatan perkembangan bicara serta gangguan perilaku komunikasi yang meliputi gangguan bahasa, bicara, suara, dan irama kelancaran sehingga pasien dapat mampu berinteraksi dengan lingkungannya secara wajar dan optimal.</p>
                        <h4>Terapi Perilaku (Behaviour Therapy) </h4>
                        <p>Terapi bagi anak dengan gangguan perkembangan perilaku yang bertujuan untuk meningkatkan kemampuan anak agar berperilaku lebih adaptif dengan lingkungan sekitar, serta mengurangi perilaku yang tidak sesuai, seperti perilaku agresif, dan lain-lain</p>
                        <h4>Terapi Orthopaedagog</h4>
                        <p>Terapi bagi anak yang mengalami kesulitan dalam mengikuti pendidikan pada umumnya, tidak mampu mengembangkan potensi secara optimal, serta memiliki prestasi belajar yang rendah, sehingga memerlukan perhatian dan bantuan khusus untuk mendapatkan hasil yang lebih baik sesuai bakat dan kemampuannya serta mengembangkan potensi akademik dan menumbuhkan sikap belajar yang positif.</p>
                        <h4>Terapi Musik</h4>
                        <p>Terapi musik bagi anak dan ibu hamil (dilakukan oleh Psikolog)</p>
                        <h4>Social Skills Group Therapy</h4>
                        <p>Dirancang khusus untuk memberikan kesempatan pada anak dengan gangguan sosial untuk melatih & meningkatkan kemampuan sosialnya di dalam situasi yang terstruktur, aman, dan suportif.</p>
